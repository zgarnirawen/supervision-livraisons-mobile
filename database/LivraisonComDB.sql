        p_villepers IN VARCHAR2, p_telpers IN NUMBER, p_login IN VARCHAR2,
        p_motP IN VARCHAR2, p_codeposte IN VARCHAR2
    ) IS
        v_login_existe NUMBER;
        v_poste_existe NUMBER;
        v_idpers NUMBER;
